
public class Calculadoraa {
    double num1, num2;
    Calculadoraa(double num, double num3){
        num1 = num;
        num2 = num3;
    }
    void soma(){
        System.out.println(num1+num2);
    }
    void subtracao(){
        System.out.println(num1-num2);
    }
    void multiplicacao(){
        System.out.println(num1*num2);
    }
    void divisao(){
        System.out.println(num1/num2);
    }
}
